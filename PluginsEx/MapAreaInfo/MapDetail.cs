        public string LeadingInfo = null;


        public override string ToString()
        {
            return CellAlpha + ":" + CellName;
        }
        public MapCell(string alpha, string name)
        {
            CellAlpha = alpha;
            CellName = name;
        }

        public void AddDrops(string drops)
        {
            if (Drops == null)
                Drops = new MapCellDrops();
            Drops.AddDrops(drops);
        }

