    public class MapArea
    {
        public string MapInfo = null;
        public Dictionary<string, MapCell> Cells = new Dictionary<string, MapCell>();
        public Dictionary<string, Point> Hotspots = new Dictionary<string, Point>();

        public void SetCells(List<MapCell> cells)
        {
            foreach (var cell in cells)
            {
                Cells[cell.CellAlpha] = cell;
            }
        }

        public string CheckPoint(Point point)
        {
            foreach (var kp in Hotspots)
            {
                if (CheckPoint(kp.Value, point))
                    return kp.Key;
            }
            return null;
        }

        bool CheckPoint(Point p1, Point p2)
        {
            double distance = Math.Sqrt((p1.X - p2.X) * (p1.X - p2.X) + (p1.Y - p2.Y) * (p1.Y - p2.Y));
            return distance < 15;
        }
        Point GetPointFromString(string s)
        {
            int p = s.IndexOf(',');
            int x = int.Parse(s.Substring(0, p));
            int y = int.Parse(s.Substring(p + 1));
            return new Point(x, y);
        }

        public void SetHotSpots(string strHotspot)
        {
            string[] hotspots = strHotspot.Split('|');
            foreach (var hotspot in hotspots)
            {
                if (hotspot != null && hotspot != "")
                {
                    string[] pair = hotspot.Split('=');
                    if (pair[0] == "@")
                        Hotspots["出击"] = GetPointFromString(pair[1]);
                    else
                        Hotspots[pair[0]] = GetPointFromString(pair[1]);
                }
            }
        }
    }

    public class MapList : Dictionary<string, MapArea>
    {
        public new MapArea this[string s]
        {
            get
            {
                MapArea area;
                if (!ContainsKey(s))
                {
                    area = new MapArea();
                    Add(s, area);
                }
                else
                    area = base[s];
                return area;
            }
        }
    }

